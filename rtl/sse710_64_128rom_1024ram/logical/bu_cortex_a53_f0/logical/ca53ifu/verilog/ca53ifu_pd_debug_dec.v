`include "ca53ifu_defs.v"

module ca53ifu_pd_debug_dec
  (
   // Clocks and resets
   input wire          clk,
   input wire          reset_n,
   input wire          a64_state_i,

   // DBG interface
   input wire [31:0]   dpu_dbg_ins_i,
   input wire          dpu_dbg_valid_i,

   output wire [39:0]  dbg_data_o,
   output wire         dbg_data_req_o
  );

  //Wire Declarations
  wire [28:0] tr_dbg_data_pd0;
  wire        tr_sf_dbg_data_pd0;
  wire        tr_m_dbg_data_pd0;
  wire        tr_n_dbg_data_pd0;
  wire        tr_d_dbg_data_pd0;
  wire        a64_undef_pd0;
  wire [28:0] dbg_data_pd0;
  wire        dbg_data_fifth_bit_pd0;
  wire        tr_fifth_loc_pd0;
  wire        t16_undef_pd0;

  wire  [5:0] dbg_sideband_pd1;
  wire        t32_undef_pd1;
  wire  [5:0] sideband;
  wire        undef_pd1;
  wire        dbg_data_en;
  wire        sf_dbg_data_pd1;

  //Reg Declarations
  reg  [28:0] dbg_data_pd1;
  reg         tr_sf_dbg_data_pd1;
  reg         tr_m_dbg_data_pd1;
  reg         tr_n_dbg_data_pd1;
  reg         tr_d_dbg_data_pd1;
  reg         a64_undef_pd1;
  reg         dbg_data_req;
  reg         dbg_data_fifth_bit_pd1;
  reg         t16_undef_pd1;

  // A64 Debug Translation Logic and Undef
  //--------------------------------------
  ca53ifu_pd_debug_dec_a64 u_ca53ifu_pd_debug_dec_a64 (
   .raw_encoding_i (dpu_dbg_ins_i[31:0]),
   .arm_to_t32_o   (tr_dbg_data_pd0[28:0]),
   .sf_o           (tr_sf_dbg_data_pd0),
   .m_o            (tr_m_dbg_data_pd0),
   .n_o            (tr_n_dbg_data_pd0),
   .d_o            (tr_d_dbg_data_pd0),
   .fifth_loc_o    (tr_fifth_loc_pd0),
   .undef_o        (a64_undef_pd0)
  );

  assign dbg_data_en  = dpu_dbg_valid_i | dbg_data_req;
  // Input data to be swizzle for register view (translated a64 already done)
  assign dbg_data_pd0 = a64_state_i ? tr_dbg_data_pd0 : {dpu_dbg_ins_i[12:0],dpu_dbg_ins_i[31:16]};

  //If A64 then and with the fifth bit otherwise in case of T32
  // it would be a zero
  assign dbg_data_fifth_bit_pd0 = a64_state_i & tr_fifth_loc_pd0;

  //Instruction is undefined if it is T16 in Debug dec (Memory view)
  assign t16_undef_pd0 = ~((dpu_dbg_ins_i[15:13] == 3'b111) & (dpu_dbg_ins_i[12:11] != 2'b00));

  //PD1 cycle - Data
  //PD1 cycle - A64 CC bits
  //PD1 cycle - Undef A64 bit
  always @(posedge clk)
    if (dbg_data_en) begin
      dbg_data_pd1[28:0]     <= dbg_data_pd0[28:0];
      tr_sf_dbg_data_pd1     <= tr_sf_dbg_data_pd0;
      tr_m_dbg_data_pd1      <= tr_m_dbg_data_pd0;
      tr_n_dbg_data_pd1      <= tr_n_dbg_data_pd0;
      tr_d_dbg_data_pd1      <= tr_d_dbg_data_pd0;
      dbg_data_fifth_bit_pd1 <= dbg_data_fifth_bit_pd0;
      a64_undef_pd1          <= a64_undef_pd0;
      t16_undef_pd1          <= t16_undef_pd0;
    end

 //PD1 cycle -Debug request
  always @(posedge clk or negedge reset_n)
    if (!reset_n)
      dbg_data_req <= 1'b0;
    else if (dbg_data_en)
      dbg_data_req <= dpu_dbg_valid_i;


 assign dbg_data_req_o = dbg_data_req;

 //If A64 then and with the sf bit otherwise in case of T32
 // it would be a zero
 assign sf_dbg_data_pd1 = a64_state_i & tr_sf_dbg_data_pd1;

  // Debug Sideband Logic and T32Undef
  //--------------------------------------
  ca53ifu_pd_debug_dec_t32 u_ca53ifu_pd_debug_dec_t32(
    .raw_encoding_i (dbg_data_pd1[28:0]),
    .fifth_bit_i    (dbg_data_fifth_bit_pd1),
    .sf_bit_i       (sf_dbg_data_pd1),
    .a64_state_i    (a64_state_i),
    .sideband_o     (dbg_sideband_pd1),
    .undef_o        (t32_undef_pd1)
  );

  //Sideband Selection
  assign  undef_pd1 = a64_state_i ? a64_undef_pd1     : (t32_undef_pd1 | t16_undef_pd1);
  assign  sideband  = undef_pd1   ? `ca53ifu_pd_undef : dbg_sideband_pd1;

  //Debug data: rebuild in memory view
  //  Bit       |    A64   | T32
  // [39]       |    sf    | 0
  // [38:36]    |  m,n,d   | 000
  // [19]       |   0      |  1
  assign dbg_data_o[39]    = a64_state_i & tr_sf_dbg_data_pd1;
  assign dbg_data_o[38:36] = a64_state_i ? {tr_m_dbg_data_pd1,tr_n_dbg_data_pd1,tr_d_dbg_data_pd1} : {3{1'b1}};
  assign dbg_data_o[35:20] = dbg_data_pd1[15:0];
  assign dbg_data_o[19]    = ~a64_state_i;
  assign dbg_data_o[18:13] = sideband;
  assign dbg_data_o[12:0]  = dbg_data_pd1[28:16];


